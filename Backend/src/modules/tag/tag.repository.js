import tagModel from '../../models/tag.model.js';

/**
 * Create a new tag
 * @param {Object} tagData - Tag data
 * @returns {Promise<Object>} Created tag
 */
export async function createTag(tagData) {
    return tagModel.create(tagData);
}

/**
 * Find tags by user
 * @param {string} userId - User ID
 * @param {number} skip - Documents to skip
 * @param {number} limit - Documents limit
 * @returns {Promise<Array>} Array of tags
 */
export async function findTagsByUser(userId, skip = 0, limit = 50) {
    return tagModel
        .find({ userId, isDeleted: false })
        .sort({ itemCount: -1, name: 1 })
        .skip(skip)
        .limit(limit)
        .lean();
}

/**
 * Find tag by ID and user
 * @param {string} tagId - Tag ID
 * @param {string} userId - User ID
 * @returns {Promise<Object>} Tag document
 */
export async function findTagById(tagId, userId) {
    return tagModel.findOne({ _id: tagId, userId, isDeleted: false }).lean();
}

/**
 * Find tag by name and user
 * @param {string} tagName - Tag name
 * @param {string} userId - User ID
 * @returns {Promise<Object>} Tag document
 */
export async function findTagByName(tagName, userId) {
    return tagModel.findOne({ name: tagName.toLowerCase(), userId, isDeleted: false }).lean();
}

/**
 * Update tag
 * @param {string} tagId - Tag ID
 * @param {string} userId - User ID
 * @param {Object} updateData - Data to update
 * @returns {Promise<Object>} Updated tag
 */
export async function updateTag(tagId, userId, updateData) {
    return tagModel.findOneAndUpdate(
        { _id: tagId, userId, isDeleted: false },
        { $set: updateData },
        { returnDocument: 'after' }
    );
}

/**
 * Soft delete tag
 * @param {string} tagId - Tag ID
 * @param {string} userId - User ID
 * @returns {Promise<Object>} Deleted tag
 */
export async function softDeleteTag(tagId, userId) {
    return tagModel.findOneAndUpdate(
        { _id: tagId, userId },
        { $set: { isDeleted: true } },
        { returnDocument: 'after' }
    );
}

/**
 * Increment tag item count
 * @param {string} tagId - Tag ID
 * @returns {Promise<Object>} Updated tag
 */
export async function incrementTagCount(tagId) {
    return tagModel.findByIdAndUpdate(tagId, { $inc: { itemCount: 1 } }, { returnDocument: 'after' });
}

/**
 * Decrement tag item count
 * @param {string} tagId - Tag ID
 * @returns {Promise<Object>} Updated tag
 */
export async function decrementTagCount(tagId) {
    return tagModel.findByIdAndUpdate(tagId, { $inc: { itemCount: -1 } }, { returnDocument: 'after' });
}

/**
 * Get or create tag
 * @param {string} userId - User ID
 * @param {string} tagName - Tag name
 * @param {boolean} isAutoGenerated - Is auto generated tag
 * @returns {Promise<Object>} Tag document
 */
export async function getOrCreateTag(userId, tagName, isAutoGenerated = false) {
    const normalizedName = tagName.toLowerCase().trim();
    const existingTag = await findTagByName(normalizedName, userId);

    if (existingTag) {
        return existingTag;
    }

    return createTag({
        userId,
        name: normalizedName,
        isAutoGenerated,
    });
}

/**
 * Get total count of tags for user
 * @param {string} userId - User ID
 * @returns {Promise<number>} Count of tags
 */
export async function countTagsByUser(userId) {
    return tagModel.countDocuments({ userId, isDeleted: false });
}

/**
 * Batch get/create tags
 * @param {string} userId - User ID
 * @param {Array<string>} tagNames - Tag names
 * @param {boolean} isAutoGenerated - Is auto generated
 * @returns {Promise<Array>} Tag documents
 */
export async function getOrCreateTags(userId, tagNames, isAutoGenerated = false) {
    const tags = [];

    for (const tagName of tagNames) {
        const tag = await getOrCreateTag(userId, tagName, isAutoGenerated);
        tags.push(tag);
    }

    return tags;
}

/**
 * Find tags by IDs
 * @param {Array<string>} tagIds - Tag IDs
 * @returns {Promise<Array>} Tags
 */
export async function findTagsByIds(tagIds) {
    return tagModel.find({ _id: { $in: tagIds }, isDeleted: false }).lean();
}
